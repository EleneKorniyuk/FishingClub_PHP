// Простые ОБЩИЕ JavaScript функции для сайта

/**
 * Функция подтверждения удаления
 * Используется при нажатии на кнопку "Удалить" в админке
 * Показывает диалоговое окно с вопросом
 * 
 * @returns {boolean} true - если пользователь нажал OK, false - если Отмена
*/

function confirmDelete() {
    return confirm('Вы уверены, что хотите удалить эту запись?');
}

/**
 * Функция проверки телефона
 * Оставляет в поле ввода только цифры и знак +
 * Вызывается при вводе (onkeyup)
 * 
 * @param {HTMLInputElement} input - поле ввода телефона
*/

function checkPhone(input) {
    // Регулярное выражение: [^\d\+] означает "всё кроме цифр и плюса"
    // Заменяем такие символы на пустую строку (удаляем)
    input.value = input.value.replace(/[^\d\+]/g, '');
}